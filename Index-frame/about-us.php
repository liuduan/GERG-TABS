    The Texas Automated Buoy System (TABS) is a coastal network of moored buoys that report nearâ€“real-time observations of currents, winds, and waves along the Texas coast. Established in 1995, the primary mission of TABS is ocean observations for the preparedness and response for oil spill. The Texas General Land Office Oil Spill Protection Program funded the system with the intent of improving data availability to oil spill trajectory modelers. In sixteen years of operation, TABS has been used for decision making purposes in over forty five events. The original capabilities of TABS, i.e., measurement of surface currents and temperatures, have been extended to the marine surface layer, the entire water column, and the sea floor. In addition to observations, a modeling component has been integrated into the TABS program. The goal is to form the core of a complete ocean observing system for Texas waters. As the nation embarks on the development of an integrated ocean observing system, TABS will continue to be an active participant of the Gulf of Mexico Coastal Ocean Observing System (GCOOS) regional association and the primary source of near-surface current measurements in the northwestern Gulf of Mexico. TABS is the only state sponsored ocean observing system in the U.S., whose primary mandate is to help protect the coastal environment from oil spills.
    
    
